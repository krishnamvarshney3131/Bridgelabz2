import static java.lang.Math.*;

public class MathStaticDemo {
    public static void main(String[] args) {
        System.out.println("Sqrt(16): " + sqrt(16));
        System.out.println("Pow(2,5): " + pow(2,5));
        System.out.println("Max(20,50): " + max(20,50));
        System.out.println("Min(5,10): " + min(5,10));
        System.out.println("Abs(-7): " + abs(-7));
    }
}

